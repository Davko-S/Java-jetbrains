package pl.davko.jetbrains.excercise.potd;

public class MagicNumber {

    public static void printStar(int amount) {
        for (int i = 0; i < amount; i++) {
            System.out.print('*');
        }
    }

    public static void printVoid(int amount) {
        for (int i = 0; i < amount; i++) {
            System.out.print(' ');
        }
    }
}
